// Modal - Cell 0
var modal0 = document.getElementById('myModal0');

var btn0 = document.getElementById("myBtn0");

var ag0 = document.getElementById("myAg0");

var dis0 = document.getElementById("myDis0");

var span0 = document.getElementsByClassName("close")[0];

btn0.onclick = function() {
    modal0.style.display = "block";
}

ag0.onclick = function() {
  if (true) {
    modal0.style.display = "none";
    $("#myBtn0").css("background", "red");
  }
}

dis0.onclick = function() {
  if (true) {
    modal0.style.display = "none";
    $("#myBtn0").css("background", "green");
    document.getElementById("myBtn0").disabled = true;
  }
}

span0.onclick = function() {
    modal0.style.display = "none";
}

window.onclick = function(event) {
    if (event.target == modal0) {
        modal0.style.display = "none";
    }
}

